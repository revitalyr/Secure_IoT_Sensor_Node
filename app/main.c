/* ── FreeRTOS Objects Initialization ───────────────────────────── */

/**
 * @brief Initialize FreeRTOS objects
 * @return true if successful, false otherwise
 */
static bool main_init_freertos_objects(void)
{
    /* Create sensor data queue */
    s_sensor_queue = xQueueCreate(MAIN_SENSOR_QUEUE_DEPTH, sizeof(FullSensorData_t));
    if (s_sensor_queue == NULL) {
        main_error_handler("Sensor queue creation failed");
        return false;
    }
    
    /* Create UART receive queue */
    s_uart_rx_queue = xQueueCreate(MAIN_UART_QUEUE_DEPTH, sizeof(uint8_t));
    if (s_uart_rx_queue == NULL) {
        main_error_handler("UART queue creation failed");
        return false;
    }
    
    /* Create UART mutex for thread-safe access */
    s_uart_mutex = xSemaphoreCreateMutex();
    if (s_uart_mutex == NULL) {
        main_error_handler("UART mutex creation failed");
        return false;
    }
    
    return true;
}

