/* ── System Initialization ───────────────────────────────────── */

/**
 * @brief Complete system initialization
 * @return true if successful, false otherwise
 */
static bool main_system_init(void)
{
    uint8_t retry_count = 0U;
    
    while (retry_count < MAIN_MAX_INIT_RETRIES) {
        /* Initialize hardware */
        if (!main_init_hardware()) {
            retry_count++;
            continue;
        }
        
        /* Initialize FreeRTOS objects */
        if (!main_init_freertos_objects()) {
            retry_count++;
            continue;
        }
        
        /* Initialize sensor subsystem */
        if (!sensor_init()) {
            retry_count++;
            continue;
        }
        
        /* Initialize web interface */
        if (!web_interface_init()) {
            retry_count++;
            continue;
        }
        
        /* All initialization successful */
        s_system_initialized = true;
        return true;
    }
    
    main_error_handler("System initialization failed after retries");
    return false;
}

/* ── Application Entry Point ───────────────────────────────────── */

/**
 * @brief Main application entry point
 * @return Should never return
 */
int main(void)
{
    /* Initialize system */
    if (!main_system_init()) {
        /* Error handler will be called from system_init */
        return -1;
    }
    
    /* Small delay to ensure all peripherals are ready */
    HAL_Delay(MAIN_STARTUP_DELAY_MS);
    
    /* Create application tasks */
    if (!main_create_tasks()) {
        /* Error handler will be called from create_tasks */
        return -1;
    }
    
    /* Start the scheduler */
    main_start_scheduler();
    
    /* Should never reach here */
    return 0;
}

/**
 * @brief Start FreeRTOS scheduler
 */
static void main_start_scheduler(void)
{
    /* Start the scheduler - this function should not return */
    vTaskStartScheduler();
    
    /* If we reach here, there was an error starting the scheduler */
    main_error_handler("Scheduler start failed");
}

/* ── Resource Cleanup ───────────────────────────────────────── */

/**
 * @brief Clean up system resources
 * 
 * This function is called during graceful shutdown or error recovery.
 */
static void main_cleanup_resources(void)
{
    /* Delete tasks if they exist */
    if (s_sensor_task_handle != NULL) {
        vTaskDelete(s_sensor_task_handle);
        s_sensor_task_handle = NULL;
    }
    
    if (s_web_task_handle != NULL) {
        vTaskDelete(s_web_task_handle);
        s_web_task_handle = NULL;
    }
    
    /* Delete FreeRTOS objects */
    if (s_sensor_queue != NULL) {
        vQueueDelete(s_sensor_queue);
        s_sensor_queue = NULL;
    }
    
    if (s_uart_rx_queue != NULL) {
        vQueueDelete(s_uart_rx_queue);
        s_uart_rx_queue = NULL;
    }
    
    if (s_uart_mutex != NULL) {
        vSemaphoreDelete(s_uart_mutex);
        s_uart_mutex = NULL;
    }
    
    s_system_initialized = false;
}

